        <div class="panel-body">
            <p>
                PolyPlanet è un sito interattivo che consente di visualizzare i cinque solidi platonici in 3D e modificarne alcuni parametri quali rotazione, opacità, colore e altro ancora.
            </p><br/>
            <h4>Tecnologie e Framework utilizzati:</h4>
            <table class="table">
                <tr>
                    <td>
                        <span class="glyphicon glyphicon-ok-circle" aria-hidden="true"></span>
                        <a href="http://www.x3dom.org">x3dom</a> Framework open-source che permette l'utilizzo del 3D nelle pagine HTML/XHTML
                    </td>
                </tr>
                <tr>
                    <td>
                        <span class="glyphicon glyphicon-ok-circle" aria-hidden="true"></span>
                        <a href="http://getbootstrap.com/">Twitter Bootstrap</a> Framework open-source per lo sviluppo di pagine web responsive
                    </td>
                </tr>
                <tr>
                    <td>
                        <span class="glyphicon glyphicon-ok-circle" aria-hidden="true"></span>
                        <a href="http://jquery.com/">jQuery</a> Librerie di supporto in Javascript di particolare aiuto nella manipolazione di pagine HTML, gestione eventi, animazioni etc.
                    </td>
                </tr>
                <tr>
                    <td>
                        <span class="glyphicon glyphicon-ok-circle" aria-hidden="true"></span>
                        <a href="http://jqueryui.com/">jQuery UI</a> Collezione di widget, temi, effetti per la personalizzazione e l'aggiunta all'interno di pagine HTML
                    </td>
                </tr>
            </table>
        </div>    
